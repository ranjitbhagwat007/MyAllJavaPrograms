package Assignments;
public class Assign7 {
	public static void main(String[] args) {
		for(int i=100;i>=0;i--) {
			System.out.println(i);
		}
		for(int i=-50;i<=-30;i++) {
			System.out.println(i);
		}
		for(int i=-10;i<=10;i++) {
			System.out.println(i);
		}
		
		for (int i=0; i<=100; i++) 
		{
            if (i%2==0) {
                System.out.println("Even number = " +i);
            }
            else {
            	System.out.println("Odd number = " +i);
            }
        }
	}
}
